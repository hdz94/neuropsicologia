								 	<p>En nuestros talleres compartimos la metodología       	
									La metodología educativa innovadora que garantiza el desarrollo psicológico del niño fue propuesta y aprobada desde el enfoque histórico-cultural y la teoría de la actividad. El conocimiento de esta metodología es indispensable para lograr una organización apropiada de actividades en base de la zona del desarrollo próximo, objetivos de cada edad y contenido específico de cada material.<br>

									Nuestros talleres representan una elaboración original propia del autor de los métodos y formas de desarrollo y enseñanza que se utilizan actualmente en México. Contamos con publicaciones recientes relacionadas con el temario de cada taller.<br>
								 	La interacción del niño a través de las actividades organizadas por el adulto se considera como un medio poderoso para garantizar la adquisición de la experiencia cultural humana y ampliar la zona de desarrollo próximo. El niño y el ser humano en general se consideran como portadores potenciales de diversas posibilidiades culturales que pueden ser compartidas a través de las actividades. El enfoque permite desarrollar formas cualitativas y flexibles para la evaluación, el desarrollo y el tratamiento.<br>
								 	</p>
								 				
								 	<p>
								 	Nuestros objetivos esenciales, durante la exposición de diversas temáticas que se exponen en los Diplomados y Talleres son:<br>

									1.-Reconsiderar el enfoque histórico-cultural y la teoría de la actividad desde el punto de vista de su valor práctico metodológico.<br>
									2.-Compartir la experiencia propia en el ámbito del desarrollo psicológico y educación.<br>
									3.-Promover una actitud crítica y creativa hacía los puntos de vista de psicología clásica y métodos tradicionales que se encuentran en el estado de estancamiento y no permiten superar problemas de aprendizaje.<br>
								 	</p>

